    const style = TextStyle(
      color: Color(0xff75729e),
      fontWeight: FontWeight.bold,
      fontSize: 14,
    );
    String text;
    switch (value.toInt()) {
      case 1:
        text = '1M';
        break;
      case 2:
        text = '2M';
        break;
      case 3:
        text = '3M';
        break;
      case 4:
        text = '5M';
        break;
      case 5:
        text = '6M';
        break;
